public class GrouppingListVO {
	
	private Integer groupMemberNo;
	private Integer roomListNo;
	private Integer orderNo;
	private Integer memberID;
	private Integer status;
	
	public Integer getGroupMemberNo() {
		return groupMemberNo;
	}
	public void setGroupMemberNo(Integer groupMemberNo) {
		this.groupMemberNo = groupMemberNo;
	}
	public Integer getRoomListNo() {
		return roomListNo;
	}
	public void setRoomListNo(Integer roomListNo) {
		this.roomListNo = roomListNo;
	}
	public Integer getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(Integer orderNo) {
		this.orderNo = orderNo;
	}
	public Integer getMemberID() {
		return memberID;
	}
	public void setMemberID(Integer memberID) {
		this.memberID = memberID;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
}
