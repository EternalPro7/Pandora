package web.orderdetail.bean;

public class OrderDetailVO {

	private Integer orderDetailNo;
	private Integer orderNo;
	private Integer packageNo;
	private Integer roomListNo;

	public Integer getOrderDetailNo() {
		return orderDetailNo;
	}
	public void setOrderDetailNo(Integer orderDetailNo) {
		this.orderDetailNo = orderDetailNo;
	}
	public Integer getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(Integer orderNo) {
		this.orderNo = orderNo;
	}
	public Integer getPackageNo() {
		return packageNo;
	}
	public void setPackageNo(Integer packageNo) {
		this.packageNo = packageNo;
	}
	public Integer getRoomListNo() {
		return roomListNo;
	}
	public void setRoomListNo(Integer roomListNo) {
		this.roomListNo = roomListNo;
	}
}
